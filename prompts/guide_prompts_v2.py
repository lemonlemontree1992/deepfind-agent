def format_entities_for_prompt(entities: dict) -> str:
    """将结构化实体格式化为Prompt描述"""

    description = "## 结构化数据（已验证）\n\n"

    # 景点
    if entities.get('attractions'):
        description += "### 景点信息\n\n"
        for i, attr in enumerate(entities['attractions'], 1):
            description += f"**景点{i}：{attr.get('name', '未知')}**\n"
            if attr.get('name_local'):
                description += f"- 当地名称：{attr['name_local']}\n"
            if attr.get('ticket') and isinstance(attr['ticket'], dict):
                description += f"- 门票：成人{attr['ticket'].get('adult', '未知')}，学生{attr['ticket'].get('student', '未知')}\n"
            if attr.get('location') and isinstance(attr['location'], dict):
                description += f"- 地址：{attr['location'].get('address', '未知')}\n"
            if attr.get('duration'):
                description += f"- 游览时长：{attr['duration']}\n"
            if attr.get('hours') and isinstance(attr['hours'], dict):
                description += f"- 开放时间：{attr['hours'].get('weekdays', '未知')}\n"
            if attr.get('highlights'):
                description += f"- 亮点：{', '.join(attr['highlights'])}\n"
            if attr.get('tips'):
                description += f"- 贴士：{', '.join(attr['tips'])}\n"
            confidence = attr.get('confidence', 0.5)
            description += f"- 置信度：{confidence:.0%}\n"
            if attr.get('source_ids'):
                description += f"- 来源：{attr['source_ids']}\n"
            description += "\n"

    # 餐厅
    if entities.get('restaurants'):
        description += "### 餐厅信息\n\n"
        for i, rest in enumerate(entities['restaurants'], 1):
            description += f"**餐厅{i}：{rest.get('name', '未知')}**\n"
            if rest.get('cuisine'):
                description += f"- 菜系：{rest['cuisine']}\n"
            if rest.get('price_range'):
                description += f"- 人均：{rest['price_range']}\n"
            if rest.get('signature_dishes'):
                description += f"- 招牌菜：{', '.join(rest['signature_dishes'])}\n"
            if rest.get('location') and isinstance(rest['location'], dict):
                description += f"- 地址：{rest['location'].get('address', '未知')}\n"
            if rest.get('hours') and isinstance(rest['hours'], dict):
                description += f"- 营业时间：{rest['hours'].get('weekdays', '未知')}\n"
            if rest.get('rating'):
                description += f"- 评分：{rest['rating']}/5\n"
            confidence = rest.get('confidence', 0.5)
            description += f"- 置信度：{confidence:.0%}\n"
            description += "\n"

    # 酒店
    if entities.get('hotels'):
        description += "### 酒店信息\n\n"
        for i, hotel in enumerate(entities['hotels'], 1):
            description += f"**酒店{i}：{hotel.get('name', '未知')}**\n"
            if hotel.get('hotel_type'):
                description += f"- 类型：{hotel['hotel_type']}\n"
            if hotel.get('price_range'):
                description += f"- 价格：{hotel['price_range']}\n"
            if hotel.get('location') and isinstance(hotel['location'], dict):
                description += f"- 地址：{hotel['location'].get('address', '未知')}\n"
            if hotel.get('rating'):
                description += f"- 评分：{hotel['rating']}/5\n"
            if hotel.get('amenities'):
                description += f"- 设施：{', '.join(hotel['amenities'])}\n"
            confidence = hotel.get('confidence', 0.5)
            description += f"- 置信度：{confidence:.0%}\n"
            description += "\n"

    # 交通
    if entities.get('transportations'):
        description += "### 交通信息\n\n"
        for trans in entities['transportations']:
            description += f"- {trans.get('type', '未知')}：{trans.get('from_location', '')} → {trans.get('to_location', '')}\n"
            if trans.get('methods'):
                for method in trans['methods']:
                    description += f"  - {method.get('name', '')}：{method.get('cost', '')}，{method.get('duration', '')}\n"
            description += "\n"

    # 旅行贴士
    if entities.get('tips'):
        description += "### 旅行贴士\n\n"
        for tip in entities['tips']:
            importance = tip.get('importance', '中')
            category = tip.get('category', '通用')
            content = tip.get('content', '')
            description += f"- [{importance}] {category}：{content}\n"
        description += "\n"

    # 其他信息
    if entities.get('weather'):
        description += f"### 天气信息\n\n{entities['weather']}\n\n"
    if entities.get('budget_estimate'):
        description += f"### 预算预估\n\n{entities['budget_estimate']}\n\n"
    if entities.get('best_time'):
        description += f"### 最佳旅行时间\n\n{entities['best_time']}\n\n"

    return description